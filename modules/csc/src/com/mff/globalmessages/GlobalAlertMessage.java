package com.mff.globalmessages;


public class GlobalAlertMessage {

	private String mMessageText;
	private String mMessageType;
	private String mMessageStartDate;
	
	public String getMessageText() {
		return mMessageText;
	}
	public void setMessageText(String pMessageText) {
		mMessageText = pMessageText;
	}
	public String getMessageType() {
		return mMessageType;
	}
	public void setMessageType(String pMessageType) {
		mMessageType = pMessageType;
	}
	public String getMessageStartDate() {
		return mMessageStartDate;
	}
	public void setMessageStartDate(String pMessageStartDate) {
		mMessageStartDate = pMessageStartDate;
	}
	
}
