CREATE TABLE MFF_USER(
  USER_ID VARCHAR2(40) NOT NULL, 
  phone_num VARCHAR2(10),
  CONSTRAINT MFF_USER_P PRIMARY KEY (USER_ID),
  CONSTRAINT MFF_USER_FK FOREIGN KEY (USER_ID) REFERENCES DPS_USER (ID)
);

CREATE TABLE MFF_ACCOUNT_LOCK(
	USER_ID VARCHAR2(40) NOT NULL,
	invalid_login_attempt_count number(1),
	account_lock_flag NUMBER(1),
	CONSTRAINT MFF_ACCOUNT_LOCK_P PRIMARY KEY (USER_ID),
	CONSTRAINT CK_account_lock_flag CHECK (account_lock_flag IN (0, 1))
);
	