CREATE TABLE MFF_STATIC_LEFT_NAV (
    ID    VARCHAR2(254) NOT NULL,
    TITLE VARCHAR2(254) NOT NULL,
    PRIMARY KEY (ID)
  );

CREATE TABLE MFF_CONTENT (
    CONTENT_ID   VARCHAR2(254) NOT NULL,
    DISPLAY_NAME VARCHAR2(254),
    PAGE_URL     VARCHAR2(254),
    TARGET       VARCHAR2(254),
    ENABLED      NUMBER(1,0),
    CONTENT_DATA CLOB,
    CONTENT_KEY VARCHAR2(254),
    PRIMARY KEY (CONTENT_ID)
  );

CREATE TABLE MFF_STATIC_LEFT_NAV_LINKS (
    ID           VARCHAR2(254) NOT NULL,
    SEQUENCE_NUM NUMBER(*,0) NOT NULL,
    LINK_ID      VARCHAR2(254),
    PRIMARY KEY (ID, SEQUENCE_NUM),
    FOREIGN KEY (ID) REFERENCES MFF_STATIC_LEFT_NAV (ID),
    FOREIGN KEY (LINK_ID) REFERENCES MFF_CONTENT (CONTENT_ID)
  );

CREATE TABLE MFF_STATIC_LEFT_NAV_RLTD_LINKS (
    LINK_ID         VARCHAR2(254) NOT NULL,
    SEQUENCE_NUM    NUMBER(*,0) NOT NULL,
    RELATED_LINK_ID VARCHAR2(254),
    PRIMARY KEY (LINK_ID, SEQUENCE_NUM),
    FOREIGN KEY (LINK_ID) REFERENCES MFF_CONTENT (CONTENT_ID),
    FOREIGN KEY (RELATED_LINK_ID) REFERENCES MFF_CONTENT (CONTENT_ID)
  );

CREATE TABLE MFF_CONTENT_ITEMS (
    CONTENT_ID      VARCHAR2(254) NOT NULL,
    SEQUENCE_NUM    NUMBER(*,0) NOT NULL,
    CONTENT_SECTION VARCHAR2(254) NOT NULL,
    PRIMARY KEY (CONTENT_ID, SEQUENCE_NUM),
    FOREIGN KEY (CONTENT_ID) REFERENCES MFF_CONTENT (CONTENT_ID),
    FOREIGN KEY (CONTENT_SECTION) REFERENCES WCM_ARTICLE (ID)
  );