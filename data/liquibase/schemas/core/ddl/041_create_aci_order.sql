CREATE TABLE ACI_ORDER
(
	ORDER_ID VARCHAR2(40) NOT NULL REFERENCES dcspp_order (order_id),
	RED_SHIELD_DEVICEID  CLOB DEFAULT null,
	IS_FRAUD NUMBER(1,0)	DEFAULT 0,
	IP_ADDRESS VARCHAR2(20),
	ACI_ORDER_ID VARCHAR2(40) DEFAULT null,
	CONSTRAINT ACI_ORDER_PK PRIMARY KEY (ORDER_ID)
);

CREATE TABLE MFF_ACI_ORDER_INFO (
    ORDER_ID  VARCHAR2(40),
    ACI_ORDER_ID   VARCHAR2(40)
  );

CREATE TABLE ACI_PROFILE_CREDIT_CARD (
	ID	VARCHAR2(40) NOT NULL REFERENCES DPS_CREDIT_CARD (ID),
	NAME_ON_CARD	VARCHAR2(255) NOT NULL,
	TOKEN_NUMBER	VARCHAR2(100),		
	CONSTRAINT ACI_PROFILE_CREDIT_CARD_PK PRIMARY KEY (ID)
);

CREATE TABLE ACI_CREDIT_CARD (
	PAYMENT_GROUP_ID VARCHAR2(40) NOT NULL REFERENCES DCSPP_PAY_GROUP(payment_group_id),
	TOKEN_NUMBER VARCHAR(100) DEFAULT NULL,
	NAME_ON_CARD VARCHAR2(255) DEFAULT NULL,
	CONSTRAINT ACI_CREDIT_CARD_PK PRIMARY KEY (PAYMENT_GROUP_ID)
);

CREATE TABLE ACI_CREDIT_CARD_STATUS
(
	STATUS_ID VARCHAR2(40) NOT NULL REFERENCES DCSPP_PAY_STATUS(STATUS_ID),
	AUTH_CODE VARCHAR2(256) DEFAULT NULL,
	CONSTRAINT ACI_CREDIT_CARD_STATUS_PK PRIMARY KEY (STATUS_ID)
);

